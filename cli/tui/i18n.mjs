export function detectLanguage() {
  const forced = (process.env.DNK_TUI_LANG ?? "").toLowerCase();
  if (forced === "zh" || forced === "en") {
    return forced;
  }
  const locale = `${process.env.LC_ALL ?? ""} ${process.env.LC_MESSAGES ?? ""} ${process.env.LANG ?? ""}`.toLowerCase();
  return locale.includes("zh") ? "zh" : "en";
}

export function initLanguage(lang) {
  setLanguage(lang);
}

export function t(state, key) {
  return COPY[state.lang][key] ?? COPY.en[key] ?? key;
}

function buildValueToKeyMap(lang) {
  const dict = COPY[lang] ?? {};
  const map = new Map();
  for (const [key, value] of Object.entries(dict)) {
    if (typeof value !== "string") {
      continue;
    }
    if (!map.has(value)) {
      map.set(value, key);
    }
  }
  return map;
}

export function toEnglishText(state, text) {
  if (typeof text !== "string" || text.length === 0) {
    return "";
  }
  const fromLang = state?.lang === "zh" ? "zh" : "en";
  if (fromLang === "en") {
    return text;
  }
  const toDict = COPY.en ?? {};
  const valueToKey = buildValueToKeyMap(fromLang);
  const segments = text.split(/(\s*·\s*)/);
  return segments
    .map((segment) => {
      if (segment.includes("·")) {
        return segment;
      }
      const key = valueToKey.get(segment.trim());
      if (!key) {
        return segment;
      }
      const mapped = toDict[key];
      return typeof mapped === "string" ? mapped : segment;
    })
    .join("");
}

export function toggleLanguage(state) {
  state.lang = state.lang === "zh" ? "en" : "zh";
  setLanguage(state.lang);
}
